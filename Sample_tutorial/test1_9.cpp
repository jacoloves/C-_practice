#include <bits/stdc++.h>
using namespace std;

int main() {

  int A, B;
  cin >> A >> B;

  // Aくんの成績
  cout << "A:";

  int i = 0;
  while (i < A) {
    cout << "]";
    i++;
  }

  cout << endl;


  // Bくんの成績
  cout << "B:";

  i = 0;
  while (i < B) {
    cout << "]";
    i++;
  }

  cout << endl;
}

